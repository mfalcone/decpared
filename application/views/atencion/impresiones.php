    <p>
        Se puede realizar el pedido de una impresión sobre lienzo sola, sin el marco de automontaje si así lo necesita.
        Aun así, deberá especificar el estilo de borde cuando realice el pedido de la impresión sobre lienzo sola.
        El borde puede ser negro, blanco o una continuación de la imagen.
        El borde será siempre de 4 cm y se añadirá, por cada lado, a las dimensiones indicadas para su lienzo. 
        <br><br>
        Ejemplo:<br>
        Si las dimensiones seleccionadas para su lienzo son 100 x 100 cm con la imagen como borde,
        el lienzo que le entreguemos medirá 108 x 108 cm. Las dimensiones máximas cuando realice el pedido de una impresión sobre lienzo sin tensor son 500 cm x 140 cm (+ borde).

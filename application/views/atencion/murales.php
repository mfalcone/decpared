<h4>Murales de pared</h4>
<div class="g nine-twelfths customer_service">
    <p>
        En cuanto a materiales e impresiones, nuestros murales de pared son de la mayor calidad.
        Todos los murales de pared se pueden pedir para que se ajusten exactamente a las dimensiones de su pared.
        Le recomendamos que encargue el mural de pared ligeramente más grande que las dimensiones de la pared,
        añadiendo unos 3-6 cm adicionales a la anchura y altura de la pared, para tener un pequeño margen a la hora de colocar el papel tapiz.
        <br><br>
        Ponemos a su disposición tres tipos diferentes de murales de pared:
    </p>
    <h2> Mural de pared (estándar)</h2>
    <p>
        Este producto se fabrica con material no tejido, que es al mismo tiempo estable y duradero.
        La cola se aplica directamente a la pared y los paneles numerados se colocan con los bordes juntos. Cada panel tiene 45 cm de ancho. 

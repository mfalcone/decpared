        <h2>Sobre Vi Agroforestry</h2>
        <p>Durante más de 30 años, Vi Agroforestry ha plantado árboles en el extremo oriental de África, una de las zonas más pobres del mundo.
        Vi Agroforestry ha plantado más de 100 millones de árboles en total y ha ayudado a casi dos millones de personas a mejorar sus condiciones de vida.

        <p>En nuestra fábrica de murales de pared nos esforzamos por minimizar nuestro impacto medioambiental y nuestro objetivo con el medio ambiente afecta a todo lo que hacemos.
        Toda la producción se realiza bajo pedido, lo que significa que únicamente producimos la cantidad solicitada de productos,
        no guardamos nada en stock ni lo desechamos de manera innecesaria. La tinta de impresión que utilizamos para nuestro papel tapiz
        no contiene disolventes ni productos químicos peligrosos, y es biodegradable. La empresa medioambiental
        Liselott Lööf AB se encarga de reciclar los materiales de desecho procedentes de la producción.
        Consulte más información sobre nuestro material en nuestra <a href="http://static.photowall.com/pdf/environmental/mpEU.pdf">política medioambiental</a>.&nbsp;<br>

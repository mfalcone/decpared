        <h2>Trabajar con Vi Agroforestry</h2>
        <p>
            Cuando compre en Decora tu Casa, también estará apoyando a la organización humanitaria sueca Vi Agroforestry. C
            ada año plantamos 5000 árboles en el área que rodea el lago Victoria, en el extremo oriental de África.
            Los árboles ayudan a luchar contra la pobreza, a mejorar el medio ambiente y a combatir el cambio climático.
